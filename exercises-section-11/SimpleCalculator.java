public class SimpleCalculator {
	public static void main(String[] args) {
		int a = 20;
		int b = 5;

		int sum = a + b;
		int minus = a - b;
		int times = a * b;
		double divided = a / b;
		int rest = a % b;

		System.out.println(sum);
		System.out.println(minus);
		System.out.println(times);
		System.out.println(divided);
		System.out.println(rest);
	}
}